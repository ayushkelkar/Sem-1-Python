# To check if a number is even or not (Very Easy program, just to check git stuff)
print("This program checks if the inputted number is even or odd")
print(" To start, input the number:")
num = int(input())
if num == 0:
    print("Number is Zero")
else:
    if num%2 == 0:
        print("Number is Even")
    else:
        print("Number is Odd")
